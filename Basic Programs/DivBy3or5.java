class DivBy3or5
{
	public static void main(String[] args) 
	{
		int num=30;
		if(num%3==0 && num%5==0)
		{
			System.out.println("hi hello");
		}
		else if( num%3==0)
		{
			System.out.println("hi");
		}
		else if( num%5==0)
		{
			System.out.println("hello");
		}
		else {
			System.out.println("bye");
			
		}
	}

}
